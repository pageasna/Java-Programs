package com.asnadocs.oddnumbers;

/**
 *
 * @author ASUS
 */
public class OddNumbers {
    public static void main(String[] args) {
        System.out.println("Odd numbers from 1 to 99 are:");
        for (int i = 1; i <= 99; i += 2) { 
            System.out.print(i + " ");
        }
    }
}
